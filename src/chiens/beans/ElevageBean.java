package chiens.beans;

public class ElevageBean {
	
    private Integer codeElevage;
    private String nomElevage;
    private String telephone;
    private String adresse;
    private String siteInternet;
    private String email;
    
	public Integer getCodeElevage() {
		return codeElevage;
	}
	
	public void setCodeElevage(Integer codeElevage) {
		this.codeElevage = codeElevage;
	}
	
	public String getNomElevage() {
		return nomElevage;
	}
	
	public void setNomElevage(String nomElevage) {
		this.nomElevage = nomElevage;
	}
	
	public String getTelephone() {
		return telephone;
	}
	
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	
	public String getAdresse() {
		return adresse;
	}
	
	public void setAdresse(String adresse) {
		this.adresse = adresse;
	}
	
	public String getSiteInternet() {
		return siteInternet;
	}
	
	public void setSiteInternet(String siteInternet) {
		this.siteInternet = siteInternet;
	}
	
	public String getEmail() {
		return email;
	}
	
	public void setEmail(String email) {
		this.email = email;
	}
    
}
